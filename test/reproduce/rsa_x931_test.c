int test_rsa_x931_sign_verify(OSSL_LIB_CTX *libCtx, const char *provName, int iterations) {
    int ret = 0;
    EVP_PKEY *pkey = NULL;
    EVP_MD_CTX *mdCtx = NULL;
    EVP_PKEY_CTX *pkeyCtx = NULL;
    unsigned char *sig = NULL;
    size_t sigLen = 0;
    unsigned char data[20];
    int i, failures = 0;
    const unsigned char *p = rsa_key_der_1024;

    /* Load RSA key */
    pkey = d2i_PrivateKey(EVP_PKEY_RSA, NULL, &p, sizeof(rsa_key_der_1024));
    if (!pkey) {
        fprintf(stderr, "Failed to load RSA key\n");
        goto cleanup;
    }

    /* Create message digest context */
    mdCtx = EVP_MD_CTX_new();
    if (!mdCtx) {
        fprintf(stderr, "Failed to create message digest context\n");
        goto cleanup;
    }

    /* Generate random data */
    if (RAND_bytes(data, sizeof(data)) != 1) {
        fprintf(stderr, "Failed to generate random data\n");
        goto cleanup;
    }

    /* Initialize for signing */
    if (EVP_DigestSignInit_ex(mdCtx, &pkeyCtx, "SHA256", libCtx, NULL, pkey, NULL) != 1) {
        fprintf(stderr, "Failed to initialize signing\n");
        goto cleanup;
    }

    /* Set padding mode to X931 */
    if (EVP_PKEY_CTX_set_rsa_padding(pkeyCtx, RSA_X931_PADDING) != 1) {
        fprintf(stderr, "Failed to set padding mode\n");
        goto cleanup;
    }

    /* Determine signature size */
    if (EVP_DigestSign(mdCtx, NULL, &sigLen, data, sizeof(data)) != 1) {
        fprintf(stderr, "Failed to determine signature size\n");
        goto cleanup;
    }

    /* Allocate memory for signature */
    sig = OPENSSL_malloc(sigLen);
    if (!sig) {
        fprintf(stderr, "Failed to allocate memory for signature\n");
        goto cleanup;
    }

    /* Run the test multiple times to try to reproduce the intermittent failure */
    for (i = 0; i < iterations; i++) {
        /* Sign the data */
        if (EVP_DigestSign(mdCtx, sig, &sigLen, data, sizeof(data)) != 1) {
            fprintf(stderr, "Failed to sign data (iteration %d)\n", i);
            failures++;
            continue;
        }

        /* Reset the context for verification */
        EVP_MD_CTX_reset(mdCtx);
        
        /* Initialize for verification */
        if (EVP_DigestVerifyInit_ex(mdCtx, &pkeyCtx, "SHA256", libCtx, NULL, pkey, NULL) != 1) {
            fprintf(stderr, "Failed to initialize verification (iteration %d)\n", i);
            failures++;
            continue;
        }

        /* Set padding mode to X931 */
        if (EVP_PKEY_CTX_set_rsa_padding(pkeyCtx, RSA_X931_PADDING) != 1) {
            fprintf(stderr, "Failed to set padding mode for verification (iteration %d)\n", i);
            failures++;
            continue;
        }

        /* Verify the signature */
        if (EVP_DigestVerify(mdCtx, sig, sigLen, data, sizeof(data)) != 1) {
            fprintf(stderr, "Signature verification failed (iteration %d)\n", i);
            failures++;
            continue;
        }

        printf(".");
        if ((i + 1) % 50 == 0) {
            printf("\n");
        }
        fflush(stdout);
    }

    if (failures > 0) {
        fprintf(stderr, "\nTest failed with %d failures out of %d iterations\n", failures, iterations);
    } else {
        printf("\nAll %d iterations passed successfully\n", iterations);
        ret = 1;
    }

cleanup:
    OPENSSL_free(sig);
    EVP_MD_CTX_free(mdCtx);
    EVP_PKEY_free(pkey);
    return ret;
}

int main(int argc, char *argv[]) {
    int ret = 1;
    OSSL_PROVIDER *defProv = NULL;
    OSSL_PROVIDER *wolfProv = NULL;
    OSSL_LIB_CTX *libCtx = NULL;
    int iterations = TEST_ITERATIONS;
    unsigned long err;

    /* Parse command line arguments */
    if (argc > 1) {
        iterations = atoi(argv[1]);
        if (iterations <= 0) {
            iterations = TEST_ITERATIONS;
        }
    }

    /* Create a new library context */
    libCtx = OSSL_LIB_CTX_new();
    if (!libCtx) {
        fprintf(stderr, "Failed to create library context\n");
        goto cleanup;
    }

    /* Load providers */
    defProv = OSSL_PROVIDER_load(libCtx, "default");
    if (!defProv) {
        fprintf(stderr, "Failed to load default provider\n");
        goto cleanup;
    }

    wolfProv = OSSL_PROVIDER_load(libCtx, "libwolfprov");
    if (!wolfProv) {
        err = ERR_get_error();
        fprintf(stderr, "Failed to load wolfProvider: %s\n", ERR_error_string(err, NULL));
        goto cleanup;
    }

    printf("Testing RSA X931 signature verification with %d iterations\n", iterations);
    
    /* Run the test with wolfProvider */
    if (test_rsa_x931_sign_verify(libCtx, "libwolfprov", iterations)) {
        printf("Test with wolfProvider succeeded\n");
        ret = 0;
    } else {
        fprintf(stderr, "Test with wolfProvider failed\n");
    }

cleanup:
    OSSL_PROVIDER_unload(wolfProv);
    OSSL_PROVIDER_unload(defProv);
    OSSL_LIB_CTX_free(libCtx);
    return ret;
}
